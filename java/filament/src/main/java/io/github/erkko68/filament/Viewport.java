package io.github.erkko68.filament;

public class Viewport {
    public int left;
    public int bottom;
    public int width;
    public int height;

    public Viewport(int left, int bottom, int width, int height) {
        this.left = left;
        this.bottom = bottom;
        this.width = width;
        this.height = height;
    }
}
